# -*- coding: utf-8 -*-
"""
ai_writer.py —— 调用 DeepSeek，让 AI 真正帮你写漫剧分镜脚本

这一版是"升级款"：模板模式只是随机填空，AI 模式才能写出有逻辑的剧情。
DeepSeek 的接口和 OpenAI 完全兼容，所以用 openai 这个库就能调。
"""

from openai import OpenAI


def 用AI生成脚本(api_key, 题材, 主角设定, 剧情梗概, 分镜数量):
    """
    把你的要求发给 DeepSeek，返回它写好的分镜脚本（一段文字）。

    参数说明：
        api_key     —— 你的 DeepSeek 密钥
        题材        —— 比如 "重生复仇 / 霸总 / 古风甜宠"
        主角设定    —— 比如 "女主：被家族抛弃的天才；男主：腹黑总裁"
        剧情梗概    —— 一句话剧情
        分镜数量    —— 想生成几个分镜
    """

    # 1) 创建客户端：base_url 指向 DeepSeek（这一行就是"连上 DeepSeek"）
    client = OpenAI(api_key=api_key, base_url="https://api.deepseek.com")

    # 2) 写"提示词"——这是 AI 生成质量的关键，相当于给编剧的工作要求
    # 【爆款公式】下面这段是核心，把"短视频留人逻辑"塞进了提示词里。
    系统提示 = (
        "你是顶级短剧/漫剧编剧，专为抖音/快手竖屏短视频写爆款分镜脚本。你严格遵守这5条铁律：\n"
        "1) 开奴3秒必须有强钩子：用冲突、悬念或巨大反差抓住观众，留不住人这条就废了；\n"
        "2) 每个分镜都要有冲突或新信息，节奏快，绝不铺垫废话；\n"
        "3) 对白要口语化、短、带情绪，能让人忍不住想看下一镜；\n"
        "4) 结尾必须留钩子或反转，勾着观众看下一集；\n"
        "5) 画面描述要具体到能直接出图：写清人物表情、动作、构图、光线、环境。\n"
        "每个分镜都必须同时有【画面描述】（喂AI绘画）和【对白】（喂AI配音）。"
    )
    用户要求 = f"""请根据以下设定，写一份 {分镜数量} 个分镜的竖屏短剧/漫剧爆款脚本。

题材：{题材}
主角设定：{主角设定}
剧情梗概：{剧情梗概}

【输出要求】
- 先给一个吸睛的【标题】（带钩子，让人想点开）。
- 第1个分镜就要是最强钩子，3秒内抓住人。
- 严格按下面格式，每个分镜之间空一行：

【分镜 序号】
场景：xxx
画面：xxx（具体到表情/动作/构图/光线，方便AI绘画）
人物：xxx
对白：xxx（口语化、短、有冲突、能勾人，可多句）
情绪：xxx

- 最后一个分镜结尾要留强钩子或反转。
"""

    # 3) 发请求给 DeepSeek（deepseek-chat 是它的对话模型）
    回复 = client.chat.completions.create(
        model="deepseek-chat",
        messages=[
            {"role": "system", "content": 系统提示},
            {"role": "user", "content": 用户要求},
        ],
        temperature=1.0,  # 1.0 比较稳；之前 1.3 太放飞，剧情会写得很"飘"
        stream=False,
    )

    # 4) 取出 AI 写好的正文返回
    return 回复.choices[0].message.content
