# -*- coding: utf-8 -*-
"""
templates.py —— 这里放"可替换内容"的核心：模板 + 词库

【你要理解的最重要概念】
所谓"系统化生成可替换内容"，本质就是：
    一段固定的模板  +  一堆会变的内容  =  批量产出
模板里用 {大括号} 标出"哪里要被替换"，运行时再把真实内容填进去。
"""

# ============ 1. 分镜模板（一个分镜长什么样） ============
# 注意 {镜号}、{场景} 这些就是"占位符"，运行时会被真实内容替换掉
分镜模板 = """\
【分镜 {镜号}】
🎬 场景：{场景}
🖼️ 画面（拿去喂AI绘画）：{画面描述}
👤 人物：{人物}
💬 对白（拿去配音）：{对白}
🎭 情绪：{情绪}
"""

# ============ 2. 词库（模板模式下，随机从这里抽词填空） ============
# 这一版"模板模式"不花钱，用来先跑通、先理解原理。
# 真正好用的剧情，等下用 AI 模式（ai_writer.py）来生成。
词库 = {
    "场景": ["豪华别墅大厅", "雨夜的天台", "破旧的出租屋", "高级餐厅包间", "公司顶层办公室", "医院走廊"],
    "画面描述": [
        "女主低头攥紧拳头，背景灯光昏暗，特写脸部",
        "男主背对镜头转身，西装革履，气场全开",
        "两人四目相对，空气凝固，慢镜头",
        "一份文件被狠狠摔在桌上，纸张散落",
    ],
    "情绪": ["隐忍", "愤怒", "震惊", "决绝", "冷笑", "崩溃"],
}


def 用模板做一个分镜(镜号, 场景, 画面描述, 人物, 对白, 情绪):
    """
    把6个内容塞进模板，返回一段排好版的分镜文字。
    .format() 会自动把模板里的 {占位符} 换成你传进来的值。
    """
    return 分镜模板.format(
        镜号=镜号,
        场景=场景,
        画面描述=画面描述,
        人物=人物,
        对白=对白,
        情绪=情绪,
    )
